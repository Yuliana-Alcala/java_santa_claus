package models;

public class Toy {
    protected String id;
    protected String title;

    public Toy(String title){
        
        this.title = title;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
    



}
